#include <iostream>
#include <iomanip>
using namespace std;
int main()
{
	int n;
	int a[13][13];
	cout << "请输入要打印的行数[3..13]" << endl;
	cin >> n;
	int t = 3 * (n + 1);
	cout << "--------------------------------------------------------------------------------" << endl;
	cout << "0         1         2         3         4         5         6         7         " << endl;
	cout << "01234567890123456789012345678901234567890123456789012345678901234567890123456789" << endl;
	cout << "--------------------------------------------------------------------------------" << endl;
	for (int i = 0; i < 13; i++)
	{
		a[i][0] = 1;
		a[i][i] = 1;
	}
	for (int i = 2; i < 13; i++)
		for (int j = 1; j < i; j++)
			a[i][j] = a[i - 1][j] + a[i - 1][j - 1];
	for (int i = 0; i < n; i++)
	{
		cout << setw(t) << a[i][0];
		for (int j = 1; j <= i; j++)
			cout << setw(6) << a[i][j];
		cout << endl;
		t = t - 3;
	}
	cout << "--------------------------------------------------------------------------------" << endl;
	cout << "0         1         2         3         4         5         6         7         " << endl;
	cout << "01234567890123456789012345678901234567890123456789012345678901234567890123456789" << endl;
	cout << "--------------------------------------------------------------------------------" << endl;
	return 0;
}
